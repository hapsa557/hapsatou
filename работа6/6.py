# Исходная строка
text = "Пример: строка с: несколькими: двоеточиями."

# Подсчет количества двоеточий
count_colons = text.count(':')

# Замена двоеточий на знак процента
modified_text = text.replace(':', '%')

# Вывод результатов
print("Измененная строка:", modified_text)
print("Количество замен:", count_colons)
