def наименьший_делитель(n):
    # Начинаем проверку делителей с 2
    делитель = 2
    while делитель <= n:
        if n % делитель == 0:  # Если делитель делит n без остатка
            return делитель
        делитель += 1

# Пример использования
n = int(input("Введите целое число (n ≥ 2): "))
print("Наименьший натуральный делитель, отличный от 1:", наименьший_делитель(n))
